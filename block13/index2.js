let num1 = -5;
let num2 = 0;
let sum = num1 + num2;

if (num1 + num2 < -1000) {
    console.log(sum + " is less than -1000");
} else {
    if (num1 + num2 === 0) {
    console.log(sum + " is equal to 0");
    } if (num1 + num2 > 0) {
    console.log(sum + " is greater than 0");
    } if (num1 + num2 > 100) {
    console.log(sum + " is greater than 100");
    } if (num1 + num2 < 0) {
    console.log(sum + " is a negative number");
    }
}
